<?php
    // increment
    $num = 0;
    $num++;
    echo $num;
    $num += 2.5;
    echo "<br>";
    echo $num;
    // abs
    $num = -100;
    $num = abs($num);
    echo "<br>";
    echo $num;
    // power
    $num = 2;
    $num = pow($num, 4);
    echo "<br>";
    echo $num;
    // square root
    $num = 144;
    $num = sqrt($num);
    echo "<br>";
    echo $num;
    // max
    $num = max(2,10,4);
    echo "<br>";
    echo $num;
    // round
    $num = 1.4;
    $num = round($num);
    echo "<br>";
    echo $num;
    // ceil
    $num = 1.1;
    $num = ceil($num);
    echo "<br>";
    echo $num;
    // floor
    $num = 1.9;
    $num = floor($num);
    echo "<br>";
    echo $num;
?>